export function getVenueBySlug(slug) {
  return venues.find((v) => v.slug === slug);
}

export function listVenues() {
  return venues.map(({ slug, name, town, county, style }) => ({ slug, name, town, county, style }));
}

// Find venues that sit in the same county as an area, or share its postcode
// prefix. Falls back to same-county matches first. Limit keeps the nearby
// grid visually balanced (3 cards by default).
export function findNearbyVenues(area, limit = 3) {
  if (!area) return [];
  const prefixes = (area.postcodes || []).map((p) => p.replace(/\s.*$/, '').replace(/\d+$/, ''));
  const sameCounty = venues.filter((v) => v.county === area.county);
  const byPostcode = venues.filter((v) => {
    const vp = (v.postcode || '').split(' ')[0].replace(/\d+$/, '');
    return vp && prefixes.some((p) => vp === p);
  });
  const combined = [...byPostcode, ...sameCounty.filter((v) => !byPostcode.includes(v))];
  return combined.slice(0, limit);
}
